    private String hitungkabisat(int tahun, String bulan) {   
        int hari=0;
        if(tahun % 4 == 0 && bulan.equals("Februari")){
            hari = 29;
        } else {
        
        switch (bulan) {
            case "Januari":
                hari = 31;
                break;
            case "Februari":
                hari = 28;
                break;
            case "Maret":
                hari = 31;
                break;
            case "April":
                hari = 30;
                break;
            case "Mei":
                hari = 31;
                break;
            case "Juni":
                hari = 30;
                break;
            case "Juli":
                hari = 31;
                break;
            case "Agustus":
                hari = 31;
                break;
            case "September":
                hari = 30;
                break;
            case "Oktober":
                hari = 31;
                break;
            case "November":
                hari = 30;
                break;
            case "Desember":
                hari = 31;
                break;
         }
        }
        kalimat = "Jumlah hari pada bulan "+bulan+" tahun "+tahun+" adalah "+hari;
        return kalimat;
    } 
    
